package com.allandroidprojects.sampleecomapp.miscellaneous;

public class SampleModelCategories {
    private String categoriesName;
    private int iconArrow;
    private int imageCategories;

    public int getImageCategories() {
        return this.imageCategories;
    }

    public void setImageCategories(int imageCategories) {
        this.imageCategories = imageCategories;
    }

    public String getCategoriesName() {
        return this.categoriesName;
    }

    public void setCategoriesName(String categoriesName) {
        this.categoriesName = categoriesName;
    }

    public int getIconArrow() {
        return this.iconArrow;
    }

    public void setIconArrow(int iconArrow) {
        this.iconArrow = iconArrow;
    }
}
